// Key translations reverse for going back to English
const GUJ_TO_ENG = {};
for (let key in TRANSLATIONS) {
    GUJ_TO_ENG[TRANSLATIONS[key]] = key;
}

const LOC_STORAGE_KEY = 'agri_lang';

class Translator {
    constructor() {
        this.currentLang = localStorage.getItem(LOC_STORAGE_KEY) || 'en';
        this.init();
    }

    init() {
        document.addEventListener('DOMContentLoaded', () => {
            this.injectToggleUI();
            this.applyLanguage(this.currentLang);
        });
    }

    injectToggleUI() {
        if (document.getElementById('langSelectorContainer')) return;

        const container = document.createElement('div');
        container.id = 'langSelectorContainer';
        container.style.cssText = `
            position: fixed;
            bottom: 20px;
            left: 20px;
            background: rgba(255, 255, 255, 0.95);
            border-radius: 30px;
            box-shadow: 0 4px 15px rgba(0,0,0,0.15);
            display: flex;
            padding: 4px;
            z-index: 999999;
            backdrop-filter: blur(5px);
            border: 1px solid rgba(0, 0, 0, 0.05);
        `;

        const btnEn = document.createElement('button');
        btnEn.id = 'langBtnEn';
        btnEn.textContent = 'EN';
        btnEn.style.cssText = this.getBtnStyle(this.currentLang === 'en');

        const btnGu = document.createElement('button');
        btnGu.id = 'langBtnGu';
        btnGu.textContent = 'ગુજરાતી';
        btnGu.style.cssText = this.getBtnStyle(this.currentLang === 'gu');

        btnEn.addEventListener('click', () => this.switchLanguage('en'));
        btnGu.addEventListener('click', () => this.switchLanguage('gu'));

        container.appendChild(btnEn);
        container.appendChild(btnGu);
        document.body.appendChild(container);
    }

    getBtnStyle(isActive) {
        return `
            border: none;
            padding: 8px 16px;
            border-radius: 20px;
            font-size: 0.85rem;
            font-weight: 700;
            cursor: pointer;
            transition: all 0.3s ease;
            outline: none;
            background: ${isActive ? 'linear-gradient(135deg, #11998e, #38ef7d)' : 'transparent'};
            color: ${isActive ? 'white' : '#666'};
        `;
    }

    switchLanguage(lang) {
        if (lang === this.currentLang) return;
        this.currentLang = lang;
        localStorage.setItem(LOC_STORAGE_KEY, lang);
        
        // Refresh styles on toggle buttons
        const btnEn = document.getElementById('langBtnEn');
        const btnGu = document.getElementById('langBtnGu');
        if (btnEn && btnGu) {
            btnEn.style.cssText = this.getBtnStyle(lang === 'en');
            btnGu.style.cssText = this.getBtnStyle(lang === 'gu');
        }

        // Apply translations
        this.applyLanguage(lang);
    }

    applyLanguage(lang) {
        this.translateDOM(document.body, lang);
    }

    translateDOM(node, lang) {
        // Translate text nodes
        if (node.nodeType === Node.TEXT_NODE) {
            const trimmed = node.nodeValue.trim();
            if (trimmed) {
                if (lang === 'gu') {
                    if (TRANSLATIONS[trimmed]) {
                        node.nodeValue = node.nodeValue.replace(trimmed, TRANSLATIONS[trimmed]);
                    }
                } else {
                    if (GUJ_TO_ENG[trimmed]) {
                        node.nodeValue = node.nodeValue.replace(trimmed, GUJ_TO_ENG[trimmed]);
                    }
                }
            }
            return;
        }

        // Ignore script tags and style tags
        if (node.nodeName === 'SCRIPT' || node.nodeName === 'STYLE') return;

        // Translate placeholders
        if (node.placeholder) {
            const trimmed = node.placeholder.trim();
            if (lang === 'gu') {
                if (TRANSLATIONS[trimmed]) node.placeholder = TRANSLATIONS[trimmed];
            } else {
                if (GUJ_TO_ENG[trimmed]) node.placeholder = GUJ_TO_ENG[trimmed];
            }
        }

        // Translate button values or inputs
        if (node.tagName === 'INPUT' && (node.type === 'button' || node.type === 'submit') && node.value) {
            const trimmed = node.value.trim();
            if (lang === 'gu') {
                if (TRANSLATIONS[trimmed]) node.value = TRANSLATIONS[trimmed];
            } else {
                if (GUJ_TO_ENG[trimmed]) node.value = GUJ_TO_ENG[trimmed];
            }
        }

        // Recursively translate children
        for (let i = 0; i < node.childNodes.length; i++) {
            this.translateDOM(node.childNodes[i], lang);
        }
    }
}

// Instantiate globally
window.TranslatorInstance = new Translator();
